package Tutorials.Animal;

public class Bird {
    public boolean Feathers;
    public boolean canFly;
    public boolean isFeathers() {
        return Feathers;
    }
    public void setFeathers(boolean feathers) {
        Feathers = feathers;
    }
    public boolean isCanFly() {
        return canFly;
    }
    public void setCanFly(boolean canFly) {
        this.canFly = canFly;
    }
    
}
