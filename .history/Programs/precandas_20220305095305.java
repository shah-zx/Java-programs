package programs;

public class precandas {
    public static void main(String[] args) {
        int j = 1 * 12 + 3 / 4;
        System.out.println(j);
        int x = 9;
        int u = 3;
        int g = (x - u) / 2;
        System.out.println(g);
        

    }
}

