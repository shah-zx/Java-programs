package Tutorials;

public class For {
    public static void main(String[] args) {
        System.out.println(""); 


        // Increasing loop 
        for (int i = 0;i < 10;i++) {
            System.out.println(i);
        }

       // decreasing loop 
       int i = 10;
       for (; i>= 0;i-- ){
        System.out.println(i);
       }

    }
}
